package main;

public class VariablePrimitiva {
    public static void main(String[] args) {
        //Primitivos
        int numero = 10;
        System.out.println("El numero es: " + numero);
        float capital = 1999999.99f;
        System.out.println("Tu capital es: " + capital);
        double decimal = 199999.99;
        System.out.println("El decimal es: " + decimal);
        boolean estado = true;
        System.out.println("El estado es: " + estado);
        String nombre = "Mauro";
        System.out.println("El nombre es: " + nombre);
    }
}
